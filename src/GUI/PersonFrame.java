    private void oprBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_oprBtnActionPerformed
        if (OppId.getText().isEmpty()) {
            dispOp.setText("Fill the Field");
        }else{
            if (Integer.parseInt(OppId.getText()) < 20000) {
                anf.setVisible(true);
            }
            else
                dispOp.setText("Not Authorized!");
        }
    }//GEN-LAST:event_oprBtnActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (calcID.getText().isEmpty()) {
            dispCal.setText("Fill the Field");
        }else{
            double res = PersonSys.makeDiscount(Integer.parseInt(calcID.getText()));
            dispCal.setText("Price is " + res);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

