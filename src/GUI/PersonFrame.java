    private void giveSalBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_giveSalBtnActionPerformed
        if (sal.getText().isEmpty()) {
            disp.setText("Fill the SALARY");
        }else{ 
            int id = Integer.parseInt(sal.getText());
            disp.setText("Given Salary to " + id + " is " +  PersonSys.giveSalary(id));
        }
    }//GEN-LAST:event_giveSalBtnActionPerformed

    private void dltBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dltBtnActionPerformed
        if (sal.getText().isEmpty()) {
            disp.setText("Fill the VISITOR");
        }else{ 
            int id = Integer.parseInt(dlt.getText());

            if (PersonSys.deleteVisitor(id)) {
                disp.setText("Visitor " + id + " is deleted!");
            }else
                disp.setText("Id is not exist or not belongs to Visitor");
        }
    }//GEN-LAST:event_dltBtnActionPerformed

